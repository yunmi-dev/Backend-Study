package variable;

public class Var5 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true, d = false; // boolean
        char e = 'A'; // 문자 하나, 작은 따옴표 사용
        char t = 64; // 문자 하나
        String s = "Hello Java"; // 문자열, 큰 따옴표 사용

        // 100, 'A', true 등 고정된 값을 리터럴(literal)이라고 함 = 변수 같은 게 아닌 "데이터 그 자체"를 의미함

        System.out.println(a);
        System.out.println(b);
        System.out.println(d);
        System.out.println(e);
        System.out.println(t);
        System.out.println(s);
    }
}
